package Control_Flow_Statements;

public class Switch_01 {
    public static void main(String[] args) {
        int value = 1;

        switch (value) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("Wrong Value");

        }
    }
}
